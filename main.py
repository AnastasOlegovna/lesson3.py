number = int(input('Введите целое число:')) # ввод данных и присвоение типа данных целое число int
rev_number = 0
number_1 = number % 10  # остаток от деления на 10

number_2 = number // 10 % 10    # деление без остатка на 10, остаток от деления на 10

number_3 = number // 100 % 10   # деление без остатка на 100, остаток от деления на 10

rev_number = number_3 + number_2*10 + number_1*100  # собираем обратное число
print('"Обратное" ему число:', rev_number)
